#include "libft.h"

char	*get_next_line(int fd)
{
	char	*s;
	char	*c;

	s = malloc(10000);
	c = s;
	while (read(fd, c, 1) > 0 && *c++ != '\n');
	if (c > s)
	{
		*c = 0;
		return (s);
	}
	free(s);
	return (NULL);
}